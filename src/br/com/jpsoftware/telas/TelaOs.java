    // metodo para imprimir uma OS
    private void imprimir_os(){
            // imprimindo uma OS
        int confirma = JOptionPane.showInternalConfirmDialog(null, "Confirma a impressão desta OS?","Atenção",JOptionPane.YES_NO_OPTION);
        if(confirma == JOptionPane.YES_OPTION){
            // emitindo relatorio com framework JasperReports
            try {
        // Usando a classe HashMap para criar um filtro
        HashMap filtro = new HashMap();
        filtro.put("os",Integer.parseInt(txtOs.getText()));
        
        
        JasperPrint print = JasperFillManager.fillReport("D:/Projetos/workspaceJava/Projeto_Infox/Relatorios/servicos.jasper",filtro,conexao);           
            // a linha abaixo exibe o relatorio atraves da classe JasperViwer
            JasperViewer.viewReport(print,false);
     
            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(null, e);
         
            }
        }                            
    }
    
    
